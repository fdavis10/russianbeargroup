/** Unique key: iso+dial (US/CA/KZ/RU share dials). */
export function dialOptionKey(entry: DialCodeEntry): string {
  return `${entry.iso}:${entry.dial}`;
}

export function findDialEntry(iso: string, dial: string): DialCodeEntry | undefined {
  return DIAL_CODES.find((e) => e.iso === iso && e.dial === dial);
}

export function composeE164(dialCode: string, national: string): string {
  let digits = national.replace(/\D/g, "").replace(/^0+/, "");
  if (dialCode && digits.startsWith(dialCode) && digits.length > dialCode.length + 4) {
    digits = digits.slice(dialCode.length);
  }
  return `+${dialCode}${digits}`;
}

/** E.164: + and 8–15 digits total after + */
export const E164_PATTERN = /^\+[1-9]\d{7,14}$/;

export function isValidE164(phone: string): boolean {
  return E164_PATTERN.test(phone);
}

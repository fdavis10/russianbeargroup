def guest_welcome(first_name: str = "") -> str:
    greeting = f", {first_name}" if first_name else ""
    return (
        f"<b>Добро пожаловать{greeting}!</b>\n"
        "\n"
        "Это <b>админ-бот</b> сайта IRC «Russian Bear».\n"
        "Он предназначен только для сотрудников и помогает:\n"
        "• получать уведомления о новых заявках с сайта;\n"
        "• быстро связаться с клиентом через Telegram или WhatsApp;\n"
        "• искать и фильтровать заявки в базе.\n"
        "\n"
        "<b>Как подключиться</b>\n"
        "Отправьте команду с секретным ключом из <code>.env</code>:\n"
        "<code>/start ваш_секретный_ключ</code>\n"
        "\n"
        "Ключ выдаёт администратор проекта. "
        "После подписки бот начнёт присылать заявки автоматически."
    )


def admin_welcome(first_name: str = "", *, active_admins: int = 1) -> str:
    greeting = f", {first_name}" if first_name else ""
    return (
        f"<b>С возвращением{greeting}!</b>\n"
        "\n"
        "Вы подписаны на уведомления о заявках с сайта "
        "IRC «Russian Bear».\n"
        f"Активных админов: <b>{active_admins}</b>\n"
        "\n"
        "<b>Что умеет бот</b>\n"
        "• мгновенно присылает новые заявки с кнопками связи;\n"
        "• ищет заявки по имени, телефону, стране или тексту;\n"
        "• показывает статистику заявок по странам.\n"
        "\n"
        "<b>Команды</b>\n"
        "/list — открыть меню поиска и фильтра заявок\n"
        "/status — проверить статус подписки\n"
        "/stop — отписаться от уведомлений\n"
        "\n"
        "<b>Как пользоваться /list</b>\n"
        "1. Нажмите «Поиск» и введите известные данные клиента.\n"
        "2. Или нажмите «Фильтр» — выберите страну или сводку по всем.\n"
        "\n"
        "Начните с команды /list 👇"
    )


def subscription_success_welcome(*, is_new: bool) -> str:
    if is_new:
        header = "<b>Подписка активирована!</b>"
        intro = "Теперь каждая новая заявка с сайта будет приходить сюда автоматически."
    else:
        header = "<b>Подписка обновлена!</b>"
        intro = "Вы снова будете получать уведомления о новых заявках."

    return (
        f"{header}\n"
        "\n"
        f"{intro}\n"
        "К каждой заявке прикреплены кнопки для связи с клиентом "
        "в Telegram и WhatsApp.\n"
        "\n"
        "<b>Основные команды</b>\n"
        "/list — поиск и фильтр всех заявок\n"
        "/status — статус подписки\n"
        "/stop — отписаться\n"
        "\n"
        "Откройте /list, чтобы найти заявку или посмотреть статистику по странам."
    )
